function myFunction() {
  var konten1 = document.getElementById("konten1");
  var readmore = document.getElementById("readmore");
  var pencet = document.getElementById("pencet");

  if (konten1.style.display === "none") {
    konten1.style.display = "inline";
    pencet.innerHTML = "Read more";
    readmore.style.display = "none";
  } else {
    konten1.style.display = "none";
    pencet.innerHTML = "Read less";
    readmore.style.display = "inline";
  }
}
